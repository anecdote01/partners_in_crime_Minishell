#include "../minishell.h"

void	builtin_pwd(void)
{
	char	*result;

	result = NULL;
	result = getcwd(result, sizeof(result));
	if (!result)
		perror("minishell ");
	else
		printf("%s\n", result);
	free(result);
	if (!is_parent())
		exit(errno);
}